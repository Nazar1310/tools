  <h2>FAQ</h2>
  <div>
    <h3>What is the difference between mortality rate and survival rate?</h3>
    <p>Survival rate is simply 100% minus the mortality rate. If your mortality rate is 8%, the survival rate is 92%. Both describe the same data from opposite perspectives.</p>
  </div>
  <div>
    <h3>Can I use this calculator for human populations?</h3>
    <p>Yes, the same formula applies to any population. However, human mortality statistics typically use standardized rates per 1,000 or 100,000 people and adjust for age distribution. This calculator gives a crude mortality rate only.</p>
  </div>
  <div>
    <h3>What if my population changes during the observation period?</h3>
    <p>For the most accurate result, use the population size at the midpoint of the observation period or the average of the starting and ending populations. This calculator uses the single population value you enter, so choose your denominator carefully.</p>
  </div>
  <div>
    <h3>Is a 0% mortality rate realistic?</h3>
    <p>In small populations over short periods, a 0% mortality rate is possible. Over longer periods or in larger groups, some mortality is expected. A consistent 0% rate may indicate incomplete death reporting rather than true absence of deaths.</p>
  </div>
  <div>
    <h3>How do I convert mortality rate to deaths per 1,000 animals?</h3>
    <p>Multiply the percentage by 10. For example, a 5% mortality rate equals 50 deaths per 1,000 animals. This format is common in livestock and wildlife reporting.</p>
  </div>
