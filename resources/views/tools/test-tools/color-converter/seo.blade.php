  <h2>How HEX and RGB Conversion Works</h2>
  <p>HEX and RGB represent the same color in different formats. HEX uses a six-digit hexadecimal code, while RGB defines a color using red, green, and blue values from 0 to 255.</p>
  <p>For HEX to RGB conversion, each pair of HEX digits is converted into a decimal number:</p>
  <ul>
    <li><strong>HEX</strong>: #RRGGBB</li>
    <li><strong>RGB</strong>: rgb(red, green, blue)</li>
  </ul>
  <p>Example logic: <strong>#1E90FF</strong> becomes <strong>rgb(30, 144, 255)</strong> because each HEX pair maps to its decimal RGB equivalent.</p>

  <h2>Example Conversion</h2>
