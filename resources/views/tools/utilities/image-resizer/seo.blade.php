  <h2>FAQ</h2>
  <div>
    <h3>Does resizing reduce image quality?</h3>
    <p>Downscaling (making an image smaller) typically does not reduce perceived quality, though some fine detail may be lost. Upscaling (making an image larger) always reduces quality because the tool must guess at missing pixel information. For best results, avoid upscaling.</p>
  </div>
  <div>
    <h3>What is the difference between resizing and cropping?</h3>
    <p>Resizing changes the overall dimensions of the entire image by scaling it up or down. Cropping removes a portion of the image to change its composition or aspect ratio. This tool resizes, it does not crop.</p>
  </div>
  <div>
    <h3>Can I resize multiple images at once?</h3>
    <p>This tool processes one image at a time. For batch resizing, you would need a desktop application or a dedicated batch processing tool.</p>
  </div>
  <div>
    <h3>What file formats are supported?</h3>
    <p>This tool supports common image formats including JPEG, PNG, and WebP. The specific formats available depend on your browser and the tool's implementation.</p>
  </div>
  <div>
    <h3>Why is my resized image blurry?</h3>
    <p>Blurriness usually occurs when you upscale a small image or when you save at a low quality setting. Start with a high-resolution source image and use a high quality setting (e.g., 90-100% for JPEG) when saving the output.</p>
  </div>
  <div>
    <h3>What does "maintain aspect ratio" mean?</h3>
    <p>It means the width and height of the image will stay in the same proportion as the original. If you change the width, the height adjusts automatically to prevent distortion. This is almost always what you want when resizing.</p>
