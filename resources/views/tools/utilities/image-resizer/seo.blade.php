<section class="seo-section">
  <h2>Understanding Image Dimensions and Aspect Ratio</h2>
  <p>Image dimensions are measured in pixels (px). A 1920x1080 image is 1920 pixels wide and 1080 pixels tall. The aspect ratio is the proportional relationship between width and height, expressed as two numbers separated by a colon (e.g., 16:9, 4:3, 1:1).</p>
  <p>When you resize an image without maintaining the aspect ratio, the image will appear stretched or squashed. For example, resizing a 1920x1080 image (16:9) to 800x800 (1:1) without locking the ratio will distort the content. Always lock the aspect ratio unless you intentionally want to change the shape of the image.</p>
  <p>Common dimension presets for different use cases:</p>
  <ul>
    <li><strong>Social media profile pictures:</strong> 400x400 pixels (square, 1:1)</li>
    <li><strong>Facebook cover photo:</strong> 820x312 pixels (approx. 2.63:1)</li>
    <li><strong>Instagram post:</strong> 1080x1080 pixels (square, 1:1)</li>
    <li><strong>Twitter header:</strong> 1500x500 pixels (3:1)</li>
    <li><strong>Website hero image:</strong> 1920x1080 pixels (16:9)</li>
    <li><strong>Email signature logo:</strong> 300x100 pixels (3:1)</li>
    <li><strong>Thumbnail image:</strong> 150x150 pixels (square, 1:1)</li>
  </ul>
