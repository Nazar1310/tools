  <h2>Resize images for web, social media, email, and print</h2>
  <p>An image resizer changes the pixel dimensions of a photo or graphic so it fits a specific use case without unnecessary cropping or oversized files. It is commonly used when an image needs to match website layout requirements, social media upload specs, email limits, or print dimensions.</p>
  <p>If you need a version of an image that is wider, taller, smaller, or sized to a target width and height, resizing is the first step. The main goal is to keep the image usable in the destination where it will appear, while preserving the aspect ratio when needed.</p>

  <h2>How image resizing works</h2>
  <p>Resizing changes the number of pixels in an image. When both width and height are adjusted together, the image may be scaled proportionally or forced into a new shape depending on the settings you choose.</p>
  <ul>
    <li><strong>Proportional resize:</strong> keeps the original aspect ratio so the image does not look stretched.</li>
    <li><strong>Fixed dimensions:</strong> sets an exact width and height, which can change the shape of the image.</li>
    <li><strong>Downscaling:</strong> reduces image size for faster loading and smaller file sizes.</li>
    <li><strong>Upscaling:</strong> increases dimensions, which may reduce sharpness if the original image is too small.</li>
  </ul>
  <p>For most web and social media uses, resizing down is preferred because it usually improves performance and keeps the image closer to its original quality.</p>

  <h2>When to resize an image</h2>
  <ul>
    <li><strong>Website images:</strong> match the space available in a page layout and reduce load time.</li>
    <li><strong>Social media posts:</strong> fit platform-specific dimensions for feeds, stories, banners, and profile images.</li>
    <li><strong>Email attachments:</strong> keep image files within size limits for sending and delivery.</li>
    <li><strong>Product listings:</strong> standardize image dimensions across an ecommerce catalog.</li>
    <li><strong>Print materials:</strong> prepare images at the correct size for flyers, posters, or documents.</li>
  </ul>

  <h2>How to choose the right dimensions</h2>
