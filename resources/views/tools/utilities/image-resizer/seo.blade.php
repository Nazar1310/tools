  <h2>FAQ</h2>

  <h3>What is the difference between resizing and cropping an image?</h3>
  <p>Resizing changes the full image dimensions. Cropping removes part of the image to create a new frame or aspect ratio. If you need the whole image smaller or larger, resize it. If you need a different composition or shape, crop it.</p>

  <h3>Will resizing an image reduce file size?</h3>
  <p>Usually, yes. Smaller dimensions often lead to a smaller file, especially for photos. However, final file size also depends on the image format and compression settings.</p>

  <h3>Can I enlarge an image without losing quality?</h3>
  <p>Only to a limited extent. Enlarging an image increases its dimensions, but it cannot recover detail that was not in the original file. Large upscaling often makes images look softer or pixelated.</p>

  <h3>What image size should I use for web pages?</h3>
  <p>The right size depends on the layout. A good approach is to resize the image close to the maximum display width on your site rather than uploading a much larger file than needed. This helps performance and keeps visuals sharp.</p>

  <h3>Should I keep the aspect ratio locked?</h3>
  <p>In most cases, yes. Locking the aspect ratio preserves the original proportions and prevents distortion. Unlock it only when you intentionally need a different width-to-height relationship.</p>

  <h3>Why does my resized image look blurry?</h3>
  <p>Blurriness usually happens when the image is enlarged beyond its original quality, compressed too heavily, or resized to dimensions that do not suit the final display. Starting with a higher-resolution source image usually produces better results.</p>
